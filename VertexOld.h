#ifndef __VERTEX_H
#define __VERTEX_H
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
#include "D3DIncludes.h"
//-------------------------------------------------------------------------------------
enum VType
{
	NONE,
	COLOR,
	TEXTURE
};
//-------------------------------------------------------------------------------------
struct Vertex
{
	VType type;
	D3DXVECTOR3 position;
	D3DXVECTOR2 boundsY;
};
//-------------------------------------------------------------------------------------
struct VertexColor
{
	D3DXVECTOR3 position;
	D3DXVECTOR4 color;
	D3DXVECTOR2 boundsY;
};
//-------------------------------------------------------------------------------------
struct VertexTexture
{
	D3DXVECTOR3 position;
	D3DXVECTOR2 tex;
	D3DXVECTOR2 boundsY;

};
//-------------------------------------------------------------------------------------
#endif//__VERTEX_H